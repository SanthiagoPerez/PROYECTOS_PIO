# == igual a
a = int(input("Ingrese el primer numero: "))
b = int(input("Ingrese el segundo numero: "))

if a == b:
    print("a y b son iguales")
else:
    print("a y b son diferentes")

#Diferencia !=
if a != b:
    print("a y b son diferentes")
else:
    print("a y b son iguales")

#Mayot que (>)
if a == b:
    print("son iguales")
elif a > b:
    print("a es mayor que b")
else:
    print("a es menor que b")